<h2 class="h2 mt-12">What is this for?</h2>
<div class="card p-4 m-12 text-left" style="max-width: 46rem;">
	This is a tool for creating many similar, complex Anki notes <em>quickly</em>.
	<br />
	<br />
	Its focused on creating and using presets of notes, which allow you to set eg. notetype and deck for
	the note with one just one click. You can also set default values for other fields, which will then
	be filled in automatically. If you have fields which often contain a little set of values, you can
	also set them as a single-selection for the field. You can also hide fields, which you don't want to
	see, but still want to be filled in automatically. Once you create the notes, you can download and
	import them in Anki with just a couple of clicks.
	<br />
	<br /> This way you only need to focus on what's important - the real content that you want to learn!
</div>
